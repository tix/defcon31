    public static List<String> getOperatorsCodeByName(String operatorName) {
        List<String> operators = new ArrayList<>();
        for (Entry<String, String> entry : OPERATORMAP.entrySet()) {
            if (entry.getValue().equalsIgnoreCase(operatorName)) {
                operators.add(entry.getKey());
            }
        }
        return operators;
    }


    /**
     * 根据运营商名字获取对应的运营商编码
     *
     * @param @param  operatorName
     * @param @return
     * @return String
     * @throws
     * @Title: getOperatorCodeByName
     * @Description: TODO
     */
    public static String getOperatorCodeByName(String operatorName) {
        StringBuilder operatorCode = new StringBuilder();
        for (Entry<String, String> entry : OPERATORMAP.entrySet()) {
            if (entry.getValue().equalsIgnoreCase(operatorName)) {
                operatorCode.append(entry.getKey()).append("|");
            }
        }
        return operatorCode.toString().substring(0, operatorCode.length() - 1);
    }

